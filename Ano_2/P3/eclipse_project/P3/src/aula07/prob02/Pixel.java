package aula07.prob02;

public class Pixel {

	private short red, green, blue;
	
	public Pixel (short red, short green, short blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	
	public short getRed() { return red; }
	public short getGreen() { return green; }
	public short getBlue() { return blue; }
	
	
}
